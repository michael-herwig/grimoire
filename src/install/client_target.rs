    #[test]
    fn path_for_global_scope_uses_vendor_native_roots() {
        let w = Path::new("/grim-home");
        let g = crate::config::scope::ConfigScope::Global;
        // Native user-level discovery paths (resolvable in any env that
        // has $HOME — the fallback keeps the workspace layout otherwise).
        if let Some(home) = crate::install::vendor::home_dir() {
            assert_eq!(
                ClientTarget::Claude.path_for(w, g, ArtifactKind::Skill, "x"),
                home.join(".claude/skills/x")
            );
            assert_eq!(
                ClientTarget::Claude.path_for(w, g, ArtifactKind::Rule, "r"),
                home.join(".claude/rules/r.md")
            );
            assert_eq!(
                ClientTarget::Copilot.path_for(w, g, ArtifactKind::Skill, "x"),
                home.join(".copilot/skills/x")
            );
            // Codex skills follow the cross-vendor `$HOME/.agents/skills`
            // standard — NOT `$CODEX_HOME` (proven by the agent path below).
            assert_eq!(
                ClientTarget::Codex.path_for(w, g, ArtifactKind::Skill, "x"),
                home.join(".agents/skills/x")
            );
            // Codex agents live under `$CODEX_HOME|~/.codex` + `agents/`; the
            // env-override order is unit-tested in vendor_codex, so only assert
            // the `~/.codex` default when CODEX_HOME is unset.
            if crate::install::vendor::env_dir("CODEX_HOME").is_none() {
                assert_eq!(
                    ClientTarget::Codex.path_for(w, g, ArtifactKind::Agent, "rev"),
                    home.join(".codex/agents/rev.toml")
                );
            }
        }
        if let Some(cfg) = crate::install::vendor::xdg_config_dir() {
            assert_eq!(
                ClientTarget::OpenCode.path_for(w, g, ArtifactKind::Skill, "x"),
                cfg.join("opencode/skills/x")
            );
        }
        // Rules stay in the workspace layout: OpenCode loads them via the
        // absolute instructions glob; Copilot has no global path (warned).
        assert_eq!(
            ClientTarget::OpenCode.path_for(w, g, ArtifactKind::Rule, "r"),
            PathBuf::from("/grim-home/.opencode/rules/r.md")
        );
        assert_eq!(
            ClientTarget::Copilot.path_for(w, g, ArtifactKind::Rule, "r"),
            PathBuf::from("/grim-home/.github/instructions/r.instructions.md")
        );
    }

