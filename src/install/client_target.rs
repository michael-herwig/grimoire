    #[test]
    fn materialize_skill_with_namespaced_metadata_renders_per_client() {
        let tmp = tempfile::tempdir().unwrap();
        let root = tmp.path().join("next");
        std::fs::create_dir_all(root.join("scripts")).unwrap();
        let canonical = "---\nname: next\ndescription: d\nmetadata:\n  keywords: a,b\n  claude.user-invocable: \"false\"\n---\n# body\n";
        std::fs::write(root.join("SKILL.md"), canonical).unwrap();
        std::fs::write(root.join("scripts/run.sh"), "echo hi\n").unwrap();

        for client in [
            ClientTarget::Claude,
            ClientTarget::OpenCode,
            ClientTarget::Copilot,
            ClientTarget::Codex,
        ] {
            let dest = tmp.path().join(format!("out-{client}/next"));
            let files = client
                .materialize(ArtifactKind::Skill, "next", &root, &dest, "p", None)
                .unwrap();
            // The rendered index is generated; the sibling stays verbatim.
            let index = dest.join("SKILL.md");
            assert!(
                files.iter().any(|f| f.path == index && f.generated),
                "{client}: SKILL.md must be generated"
            );
            assert!(
                files
                    .iter()
                    .any(|f| f.path == dest.join("scripts/run.sh") && !f.generated),
                "{client}: siblings stay verbatim"
            );
            let doc = std::fs::read_to_string(&index).unwrap();
            assert!(
                !doc.contains("claude.user-invocable"),
                "{client}: namespaced key lifted/dropped"
            );
            assert!(doc.contains("keywords: a,b"), "{client}: plain metadata kept");
            if client == ClientTarget::Claude {
                assert!(doc.contains("user-invocable: false"), "native typed field for Claude");
            } else {
                assert!(!doc.contains("user-invocable"), "{client}: clean universal SKILL.md");
            }
        }
    }

