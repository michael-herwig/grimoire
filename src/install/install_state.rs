    // ── W5: `.copilot`-twice denormalization absence (defect class 4) ─────────

    /// REGRESSION (W5): a record with three distinct `ClientOutput`s
    /// (claude/copilot/opencode, distinct content hashes) round-trips with all
    /// three clients and hashes preserved, AND the serialized record carries NO
    /// top-level `target` or `content_hash` keys — proof the V1 denormalization
    /// (a top-level mirror of the first client) is gone. `outputs` is the single
    /// source of truth, so a second `.copilot` output can never collapse onto a
    /// denormalized mirror of the first.
    #[test]
    fn three_outputs_round_trip_with_no_denormalized_top_level_keys() {
        let dir = tempfile::tempdir().unwrap();
        let path = dir.path().join("state.json");

        let mut st = InstallState::load(&path).unwrap();
        st.record(InstallRecord {
            kind: ArtifactKind::Rule,
            name: "tri".to_string(),
            pinned: pinned("tri", 'a'),
            outputs: vec![
                ClientOutput {
                    client: "claude".to_string(),
                    target: AnchoredPath {
                        anchor: PathAnchor::Workspace,
                        relative: ".claude/rules/tri.md".to_string(),
                    },
                    content_hash: Algorithm::Sha256.hash(b"claude-bytes"),
                    support_dir: None,
                },
                ClientOutput {
                    client: "copilot".to_string(),
                    target: AnchoredPath {
                        anchor: PathAnchor::Workspace,
                        relative: ".github/instructions/tri.instructions.md".to_string(),
                    },
                    content_hash: Algorithm::Sha256.hash(b"copilot-bytes"),
                    support_dir: None,
                },
                ClientOutput {
                    client: "opencode".to_string(),
                    target: AnchoredPath {
                        anchor: PathAnchor::Workspace,
                        relative: ".opencode/rules/tri.md".to_string(),
                    },
                    content_hash: Algorithm::Sha256.hash(b"opencode-bytes"),
                    support_dir: None,
                },
            ],
        });
        st.save().unwrap();

        // Reload: all three distinct clients + distinct hashes survive.
        let reloaded = InstallState::load(&path).unwrap();
        let rec = reloaded
            .get(ArtifactKind::Rule, "tri")
            .expect("present after round-trip");
        assert_eq!(rec.outputs.len(), 3, "all three outputs survive the round-trip");

        let clients: std::collections::BTreeSet<&str> = rec.outputs.iter().map(|o| o.client.as_str()).collect();
        assert_eq!(clients.len(), 3, "three distinct clients");
        assert!(clients.contains("claude") && clients.contains("copilot") && clients.contains("opencode"));

        let hashes: std::collections::BTreeSet<&Digest> = rec.outputs.iter().map(|o| &o.content_hash).collect();
        assert_eq!(hashes.len(), 3, "three distinct content hashes");

        // Defect-class-4 proof: parse the on-disk bytes and assert the
        // serialized record has NO top-level `target` or `content_hash` keys
        // (the V1 denormalized mirror is gone).
        let bytes = std::fs::read(&path).unwrap();
        let value: serde_json::Value = serde_json::from_slice(&bytes).unwrap();
        let record_value = value["records"][0]
            .as_object()
            .expect("each record serializes as a JSON object");
        assert!(
            !record_value.contains_key("target"),
            "the serialized record must NOT carry a top-level `target` key (denorm gone)"
        );
        assert!(
            !record_value.contains_key("content_hash"),
            "the serialized record must NOT carry a top-level `content_hash` key (denorm gone)"
        );
    }
