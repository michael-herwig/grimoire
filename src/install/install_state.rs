//! Persisted install state: what is installed, pinned to what, hashed to
//! what, and where.
//!
//! ## V2 on-disk layout (current)
//!
//! * **Global scope** — `$GRIM_HOME/state/global.json`. The location is
//!   unchanged from V1; records now carry anchor-relativized paths.
//! * **Project scope** — `<workspace>/.grimoire/state.json`. The directory
//!   (`<workspace>/.grimoire/`) is the collision key so two workspaces
//!   sharing one `$GRIM_HOME` can never collide (the old V1 key was
//!   `sha256(canonicalize(config_path))`, which is identical across
//!   machines for a shared `/workspace`).
//!
//! The `$GRIM_HOME/state/projects/<sha256>.json` path lives on **only** as
//! the migration-discovery helper [`InstallState::legacy_project_path`].
//! No new code ever writes to that path.
//!
//! ## Format
//!
//! The file is version-enveloped via `serde_repr` (an unknown version is
//! rejected, never silently reset) and written through the shared
//! atomic-write primitive.
//!
//! The in-memory map is keyed by `(ArtifactKind, name)`. JSON object keys
//! cannot be a tuple, so on disk the records are a list sorted by
//! `(kind, name)` for byte-stability; the list is folded back into the map
//! on load.

use std::collections::BTreeMap;
use std::path::{Path, PathBuf};
use std::str::FromStr as _;

use serde::{Deserialize, Serialize};
use serde_repr::{Deserialize_repr, Serialize_repr};
use sha2::Digest as _;

use crate::config::scope::ConfigScope;
use crate::install::client_target::ClientTarget;
use crate::install::content_hash::footprint_hash;
use crate::install::path_anchor::{AnchorError, AnchorRoots, AnchoredPath};
use crate::oci::{ArtifactKind, Digest, PinnedIdentifier};
use crate::store::atomic_write::atomic_write;

/// On-disk install-state envelope version.
///
/// Closed internal on-disk discriminant — not `#[non_exhaustive]`, per the
/// project convention. An unknown discriminant fails deserialization at
/// the `serde_repr` layer with no silent fallback.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize_repr, Deserialize_repr)]
#[repr(u8)]
pub enum InstallStateVersion {
    /// Version 1 of the on-disk format (absolute paths, denormalized
    /// top-level `target`/`content_hash`).
    V1 = 1,
    /// Version 2: anchor-relativized, portable outputs; the denormalized
    /// top-level fields are gone (`outputs` is the single source of truth).
    V2 = 2,
}

/// One client target's materialized output for an artifact.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(deny_unknown_fields)]
pub struct ClientOutput {
    /// The client target name (`claude`/`opencode`/`copilot`).
    pub client: String,
    /// The anchor-relativized on-disk path the artifact was materialized to
    /// for this client. For a rule this is the index `<name>.md` file beside
    /// any [`Self::support_dir`].
    pub target: AnchoredPath,
    /// The content hash of the materialized output at install time. For a
    /// generated (transformed) target this is the hash of the *expected*
    /// generated bytes, so a user edit is detected as drift. For a
    /// multi-file rule it covers the combined index + support-dir footprint.
    pub content_hash: Digest,
    /// For a multi-file rule, the anchor-relativized sibling support
    /// directory installed beside the index `target` (`<parent>/<name>/`).
    /// Absent for skills and single-file rules.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub support_dir: Option<AnchoredPath>,
}

impl ClientOutput {
    /// Recompute this output's on-disk content hash over its full footprint
    /// (the resolved target plus, for a multi-file rule, its sibling support
    /// dir). The integrity gate compares this against [`Self::content_hash`].
    ///
    /// # Errors
    ///
    /// [`AnchorError`] from resolving/validating the anchored target or
    /// support dir, or an I/O error from walking the footprint.
    pub fn current_hash(&self, roots: &AnchorRoots) -> Result<Digest, AnchorError> {
        let target = self.resolved_target(roots)?;
        let support = self.resolved_support_dir(roots)?;
        footprint_hash(&target, support.as_deref()).map_err(|source| AnchorError::Io { path: target, source })
    }

    /// Resolve + validate this output's anchored target into an absolute
    /// on-disk path (containment-guaranteed).
    ///
    /// # Errors
    ///
    /// [`AnchorError`] from the two-layer containment guard.
    pub fn resolved_target(&self, roots: &AnchorRoots) -> Result<PathBuf, AnchorError> {
        self.target.resolve(roots)
    }

    /// Resolve + validate this output's anchored support dir, when present.
    ///
    /// # Errors
    ///
    /// [`AnchorError`] from the two-layer containment guard.
    pub fn resolved_support_dir(&self, roots: &AnchorRoots) -> Result<Option<PathBuf>, AnchorError> {
        self.support_dir.as_ref().map(|dir| dir.resolve(roots)).transpose()
    }
}

/// One installed artifact's recorded state.
///
/// `outputs` is the single source of truth for every materialized client
/// target (no denormalized top-level mirror of the first client). It is
/// never empty for any consumer: `load` always converts a legacy V1 record
/// in memory so a synthesized output is present.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(deny_unknown_fields)]
pub struct InstallRecord {
    /// Skill or rule.
    pub kind: ArtifactKind,
    /// Config binding name.
    pub name: String,
    /// The pin the artifact was installed from.
    pub pinned: PinnedIdentifier,
    /// Per-client materialized outputs (the single source of truth).
    pub outputs: Vec<ClientOutput>,
}

/// Versioned envelope persisted at the scope's state file.
#[derive(Debug, Clone, Serialize, Deserialize)]
#[serde(deny_unknown_fields)]
struct InstallStateFile {
    version: InstallStateVersion,
    /// Records sorted by `(kind, name)` for byte-stable output.
    records: Vec<InstallRecord>,
}

/// Minimal envelope used to peek the on-disk version discriminant before
/// committing to a V1 or V2 deserialization. Deliberately tolerant of the
/// other fields (no `deny_unknown_fields`) so the same bytes can be re-read
/// into the version-specific struct afterward.
#[derive(Debug, Deserialize)]
struct VersionProbe {
    version: InstallStateVersion,
}

/// Secondary probe used only when [`VersionProbe`] fails: reads the raw
/// `version` integer so a file written by a newer grim (a discriminant the
/// `serde_repr` [`InstallStateVersion`] enum does not know) can be told apart
/// from a genuinely-corrupt file and surfaced as an actionable "upgrade grim"
/// message rather than an opaque `InvalidData`.
#[derive(Debug, Deserialize)]
struct RawVersionProbe {
    version: u8,
}

/// The V1 on-disk envelope — read-only, used solely to deserialize a legacy
/// file before converting it to the V2 in-memory shape. Never written.
///
/// `deny_unknown_fields` on the V2 [`InstallStateFile`] is what makes a V2
/// blob fail to deserialize into the V1 wire types (the rollback trap): the
/// V1 records carry `target`/`content_hash`/`clients`, which a V2 file does
/// not have, and a V2 file's `outputs` is unknown here.
#[derive(Debug, Deserialize)]
#[serde(deny_unknown_fields)]
struct InstallStateFileV1 {
    #[allow(dead_code)]
    version: InstallStateVersion,
    records: Vec<InstallRecordV1>,
}

/// One V1 on-disk record: denormalized top-level `target`/`content_hash`
/// (a mirror of the first client) plus an optional `clients` list.
#[derive(Debug, Deserialize)]
#[serde(deny_unknown_fields)]
struct InstallRecordV1 {
    kind: ArtifactKind,
    name: String,
    pinned: PinnedIdentifier,
    /// Denormalized absolute target (mirror of `clients[0]`).
    target: PathBuf,
    /// Denormalized content hash (mirror of `clients[0]`).
    content_hash: Digest,
    /// Per-client outputs. Empty in the earliest V1 files (the denormalized
    /// fields are then the only source), populated in later V1 files.
    #[serde(default)]
    clients: Vec<ClientRecordV1>,
}

/// One V1 on-disk client record: an absolute `target`/`support_dir` and the
/// content hash captured at install time.
#[derive(Debug, Deserialize)]
#[serde(deny_unknown_fields)]
struct ClientRecordV1 {
    client: String,
    target: PathBuf,
    content_hash: Digest,
    #[serde(default)]
    support_dir: Option<PathBuf>,
}

/// The in-memory record map, keyed by `(kind, name)`. Aliased so the
/// `(map, lossy)` parse return and the struct field share one name (and to
/// keep the parse signature under `clippy::type_complexity`).
type RecordMap = BTreeMap<(ArtifactKind, String), InstallRecord>;

/// In-memory install state for one scope, with its backing file path.
#[derive(Debug, Clone)]
pub struct InstallState {
    path: PathBuf,
    records: RecordMap,
    /// Set when a V1→V2 conversion dropped any output or whole record
    /// (unanchorable). A lossy migration must NOT reap the legacy file —
    /// it is the only remaining record of what was lost, so it is kept as
    /// a recovery breadcrumb. Default `false` (empty state, V2 load, or a
    /// clean V1 conversion that lost nothing).
    legacy_migration_lossy: bool,
}

impl InstallState {
    /// The global-scope state file under `state_dir`.
    pub fn global_path(state_dir: &Path) -> PathBuf {
        state_dir.join("global.json")
    }

    /// The project-scope state file: `<workspace>/.grimoire/state.json`.
    /// The directory location is the collision key (replaces the old
    /// `sha256(config_path)` filename), so two workspaces sharing one
    /// `$GRIM_HOME` never collide.
    pub fn project_state_path(workspace: &Path) -> PathBuf {
        workspace.join(".grimoire").join("state.json")
    }

    /// The legacy project-scope state file for `canonical_config_path` under
    /// `state_dir`: `projects/<sha256-of-path>.json`. Retained only as the
    /// V1→V2 migration discovery helper (the sole surviving use of the old
    /// hash formula).
    pub fn legacy_project_path(state_dir: &Path, canonical_config_path: &Path) -> PathBuf {
        let mut hasher = sha2::Sha256::new();
        hasher.update(canonical_config_path.to_string_lossy().as_bytes());
        let hex = hex::encode(hasher.finalize());
        state_dir.join("projects").join(format!("{hex}.json"))
    }

    /// Create the project `.grimoire/` directory and write a self-managed
    /// `.grimoire/.gitignore` (`*`) when absent, so machine-local install
    /// state is never committed (uv/pixi pattern). Idempotent: never
    /// overwrites a user-edited gitignore. Called from mutating project-scope
    /// seams before `save`, never from read-only consumers.
    ///
    /// # Errors
    ///
    /// An I/O failure creating the directory or writing the gitignore.
    /// Returns a descriptive error when `.grimoire` already exists as a
    /// regular file (not a directory) so grim can create its state directory.
    pub fn ensure_project_state_dir(workspace: &Path) -> std::io::Result<()> {
        let dir = workspace.join(".grimoire");

        // Detect the case where `.grimoire` exists as a file — `create_dir_all`
        // would return an opaque OS error ("not a directory"). Surface a
        // descriptive message instead so users know what to remove.
        if dir.exists() && !dir.is_dir() {
            return Err(std::io::Error::new(
                std::io::ErrorKind::AlreadyExists,
                format!(
                    "'.grimoire' exists as a file at '{}'; remove it so grim can create its state directory",
                    dir.display()
                ),
            ));
        }

        std::fs::create_dir_all(&dir)?;

        // Write the self-managed gitignore atomically using create_new: open
        // succeeds only when the file does not yet exist. On AlreadyExists the
        // file is already there (possibly hand-edited) — treat as success,
        // never clobber. Any other error is propagated.
        //
        // This avoids the TOCTOU race in a check-then-write pattern: two
        // concurrent grim invocations both see the file absent, then one writes
        // and the other overwrites — create_new makes that impossible because
        // only one opener wins the O_EXCL create.
        let gitignore = dir.join(".gitignore");
        match std::fs::OpenOptions::new()
            .write(true)
            .create_new(true)
            .open(&gitignore)
        {
            Ok(mut file) => {
                use std::io::Write as _;
                file.write_all(b"*\n")?;
            }
            Err(e) if e.kind() == std::io::ErrorKind::AlreadyExists => {
                // Already present (grim-managed or hand-edited) — idempotent, do nothing.
            }
            Err(e) => return Err(e),
        }

        Ok(())
    }

    /// An empty state backed by `scope_path` (nothing installed yet).
    pub fn empty(scope_path: &Path) -> Self {
        Self {
            path: scope_path.to_path_buf(),
            records: BTreeMap::new(),
            legacy_migration_lossy: false,
        }
    }

    /// Load the state at `scope_path`. An absent file yields empty state
    /// (a never-installed scope is not an error).
    ///
    /// # CAUTION — V2 only
    ///
    /// This method is V2-only: a V1 file at `scope_path` is rejected with
    /// [`std::io::ErrorKind::InvalidData`] because no [`AnchorRoots`] are
    /// available to relativize its absolute paths. **All production scope
    /// loading must go through the dedicated entry points**:
    /// - [`Self::load_project`] — for project scope (handles V1 legacy fallback)
    /// - [`Self::load_global`] — for global scope (threads `roots` so V1 converts)
    ///
    /// Calling bare `load` on a file that may be V1 is a logic error.
    ///
    /// # Errors
    ///
    /// [`std::io::Error`] for a read failure; an unknown version or a
    /// corrupt file is surfaced as [`std::io::ErrorKind::InvalidData`].
    pub fn load(scope_path: &Path) -> std::io::Result<Self> {
        let bytes = match std::fs::read(scope_path) {
            Ok(b) => b,
            Err(e) if e.kind() == std::io::ErrorKind::NotFound => {
                return Ok(Self {
                    path: scope_path.to_path_buf(),
                    records: BTreeMap::new(),
                    legacy_migration_lossy: false,
                });
            }
            Err(e) => return Err(e),
        };
        let (records, legacy_migration_lossy) = Self::records_from_bytes(&bytes, scope_path, None)?;
        Ok(Self {
            path: scope_path.to_path_buf(),
            records,
            legacy_migration_lossy,
        })
    }

    /// Load the global-scope state at `scope_path`, threading `roots` so a
    /// legacy V1 `global.json` converts to anchored outputs in memory (no
    /// disk write). An absent file yields empty state.
    ///
    /// This is the global-scope counterpart of [`Self::load_project`]: bare
    /// [`Self::load`] cannot anchor a V1 global file (it has no vendor roots),
    /// so a global V1 file would be rejected as `InvalidData`. Threading
    /// `roots` lets the global vendor anchors (`ClaudeRoot` / `CopilotRoot` /
    /// `OpenCodeSkills` / `GrimHome`) classify each legacy absolute target.
    ///
    /// # Errors
    ///
    /// [`std::io::Error`] for a read failure; an unknown version or a corrupt
    /// file is surfaced as [`std::io::ErrorKind::InvalidData`].
    pub fn load_global(scope_path: &Path, roots: &AnchorRoots) -> std::io::Result<Self> {
        let bytes = match std::fs::read(scope_path) {
            Ok(b) => b,
            Err(e) if e.kind() == std::io::ErrorKind::NotFound => {
                return Ok(Self {
                    path: scope_path.to_path_buf(),
                    records: BTreeMap::new(),
                    legacy_migration_lossy: false,
                });
            }
            Err(e) => return Err(e),
        };
        let (records, legacy_migration_lossy) =
            Self::records_from_bytes(&bytes, scope_path, Some((ConfigScope::Global, roots)))?;
        Ok(Self {
            path: scope_path.to_path_buf(),
            records,
            legacy_migration_lossy,
        })
    }

    /// Parse raw state bytes into the in-memory record map, converting a V1
    /// file to V2 in memory when `convert` supplies the `(scope, roots)` the
    /// anchoring step needs. A V1 file with `convert == None` (global `load`,
    /// where vendor roots are not threaded yet) is rejected as
    /// [`std::io::ErrorKind::InvalidData`] rather than silently producing
    /// empty outputs.
    ///
    /// The returned `bool` is the lossy flag: `true` when a V1 conversion
    /// dropped any output or whole record (a V2 file is never lossy). The
    /// caller threads it into [`Self::legacy_migration_lossy`] so a lossy
    /// migration skips the legacy-file reap.
    fn records_from_bytes(
        bytes: &[u8],
        path: &Path,
        convert: Option<(ConfigScope, &AnchorRoots)>,
    ) -> std::io::Result<(RecordMap, bool)> {
        let probe: VersionProbe = match serde_json::from_slice(bytes) {
            Ok(probe) => probe,
            Err(e) => {
                // The `serde_repr` discriminant probe failed. Before surfacing
                // an opaque `InvalidData`, try a raw integer probe: a file
                // written by a newer grim carries a version this binary does
                // not recognize. Distinguish that from genuine corruption so
                // the user gets an actionable "upgrade grim" message.
                if let Ok(raw) = serde_json::from_slice::<RawVersionProbe>(bytes)
                    && raw.version > InstallStateVersion::V2 as u8
                {
                    return Err(std::io::Error::new(
                        std::io::ErrorKind::InvalidData,
                        format!(
                            "install state at {} was written by a newer version of grim (version {}); upgrade grim to read it",
                            path.display(),
                            raw.version
                        ),
                    ));
                }
                return Err(std::io::Error::new(std::io::ErrorKind::InvalidData, e));
            }
        };
        let (records, lossy) = match probe.version {
            InstallStateVersion::V2 => {
                let file: InstallStateFile = serde_json::from_slice(bytes)
                    .map_err(|e| std::io::Error::new(std::io::ErrorKind::InvalidData, e))?;
                (file.records, false)
            }
            InstallStateVersion::V1 => {
                let Some((scope, roots)) = convert else {
                    return Err(std::io::Error::new(
                        std::io::ErrorKind::InvalidData,
                        "legacy V1 install state cannot be loaded without anchor roots",
                    ));
                };
                let file: InstallStateFileV1 = serde_json::from_slice(bytes)
                    .map_err(|e| std::io::Error::new(std::io::ErrorKind::InvalidData, e))?;
                convert_v1_records(file.records, scope, roots)
            }
        };
        Ok((
            records.into_iter().map(|r| ((r.kind, r.name.clone()), r)).collect(),
            lossy,
        ))
    }

    /// The recorded state for `(kind, name)`, if installed.
    pub fn get(&self, kind: ArtifactKind, name: &str) -> Option<&InstallRecord> {
        self.records.get(&(kind, name.to_string()))
    }

    /// Iterate every recorded artifact (order: `(kind, name)`).
    ///
    /// Used by the catalog badge derivation, which keys on the pinned
    /// repository rather than the config binding name.
    pub fn iter_records(&self) -> impl Iterator<Item = &InstallRecord> {
        self.records.values()
    }

    /// Whether the V1→V2 conversion that produced this state dropped any
    /// output or whole record (unanchorable). A mutating command must skip
    /// the legacy-file reap when this is `true`, preserving the legacy file
    /// as a recovery breadcrumb for the lost records.
    pub fn legacy_migration_lossy(&self) -> bool {
        self.legacy_migration_lossy
    }

    /// Insert or replace the record for an artifact.
    pub fn record(&mut self, record: InstallRecord) {
        self.records.insert((record.kind, record.name.clone()), record);
    }

    /// Drop the record for `(kind, name)`, returning it if present.
    /// Idempotent — removing an absent record is a no-op (`None`).
    pub fn remove(&mut self, kind: ArtifactKind, name: &str) -> Option<InstallRecord> {
        self.records.remove(&(kind, name.to_string()))
    }

    /// Load the project-scope state for a workspace, with a V1 legacy
    /// fallback.
    ///
    /// This is the project-scope variant of [`Self::load`] that also checks
    /// for a legacy `$GRIM_HOME/state/projects/<sha>.json` file when the
    /// new `<workspace>/.grimoire/state.json` is absent. If found, the V1
    /// file is converted in memory (no disk write). If neither file exists,
    /// an empty state is returned.
    ///
    /// # Errors
    ///
    /// [`std::io::Error`] for a read failure; an unknown version or a
    /// corrupt file is surfaced as [`std::io::ErrorKind::InvalidData`].
    pub fn load_project(workspace: &Path, grim_home: &Path, config_path: &Path) -> std::io::Result<Self> {
        let new_path = Self::project_state_path(workspace);

        // Project-scope anchoring only ever uses the `Workspace` root, so the
        // vendor roots stay `None` (a V1 client target outside the workspace
        // is correctly classified as unanchorable and dropped). `grim_home`
        // is supplied for completeness / future global threading.
        let roots = AnchorRoots {
            workspace: workspace.to_path_buf(),
            grim_home: grim_home.to_path_buf(),
            claude_root: None,
            copilot_root: None,
            opencode_skills: None,
            agents_skills: None,
            codex_root: None,
        };
        let convert = (ConfigScope::Project, &roots);

        // Primary location: <workspace>/.grimoire/state.json. Present ⇒ read
        // it (V2 direct, or V1 converted in memory — never written here).
        match std::fs::read(&new_path) {
            Ok(bytes) => {
                let (records, legacy_migration_lossy) = Self::records_from_bytes(&bytes, &new_path, Some(convert))?;
                return Ok(Self {
                    path: new_path,
                    records,
                    legacy_migration_lossy,
                });
            }
            Err(e) if e.kind() == std::io::ErrorKind::NotFound => {}
            Err(e) => return Err(e),
        }

        // Fallback: the legacy $GRIM_HOME/state/projects/<sha>.json file from
        // before the relocation. Discovered via the legacy sha formula
        // (`sha256(canonicalize(config_path))`, matching the V1 writer and the
        // reap step) and converted in memory only — nothing is written, so a
        // read-only consumer (status/search/TUI) sees migrated outputs without
        // a write.
        let state_dir = grim_home.join("state");
        let canonical = std::fs::canonicalize(config_path).unwrap_or_else(|_| config_path.to_path_buf());
        let legacy_path = Self::legacy_project_path(&state_dir, &canonical);
        match std::fs::read(&legacy_path) {
            Ok(bytes) => {
                let (records, legacy_migration_lossy) = Self::records_from_bytes(&bytes, &legacy_path, Some(convert))?;
                // The in-memory state is still backed by the NEW location, so a
                // later mutating `save` relocates it (the legacy file is reaped
                // by the migration persist step, not here — and only when the
                // conversion was non-lossy).
                Ok(Self {
                    path: new_path,
                    records,
                    legacy_migration_lossy,
                })
            }
            Err(e) if e.kind() == std::io::ErrorKind::NotFound => Ok(Self {
                path: new_path,
                records: BTreeMap::new(),
                legacy_migration_lossy: false,
            }),
            Err(e) => Err(e),
        }
    }

    /// Atomically persist the state to its backing file.
    ///
    /// Records are sorted by `(name, kind)` ascending before serialization;
    /// within each record, `outputs` is sorted by `client` ascending. This
    /// guarantees identical JSON regardless of insertion order.
    ///
    /// # Errors
    ///
    /// Serialization or atomic-write I/O failure.
    pub fn save(&self) -> std::io::Result<()> {
        let mut records: Vec<InstallRecord> = self.records.values().cloned().collect();
        // Sort records by `(name, kind)` ascending, and each record's outputs
        // by `client` ascending, so the serialized bytes are identical
        // regardless of insertion order.
        records.sort_by(|a, b| (a.name.as_str(), a.kind).cmp(&(b.name.as_str(), b.kind)));
        for record in &mut records {
            record.outputs.sort_by(|a, b| a.client.cmp(&b.client));
        }
        let file = InstallStateFile {
            version: InstallStateVersion::V2,
            records,
        };
        let bytes =
            serde_json::to_vec_pretty(&file).map_err(|e| std::io::Error::new(std::io::ErrorKind::InvalidData, e))?;
        atomic_write(&self.path, &bytes)
    }

    /// Best-effort removal of the legacy `$GRIM_HOME/state/projects/<sha>.json`
    /// file for a project scope, after the state has been relocated to
    /// `<workspace>/.grimoire/state.json` via [`Self::save`].
    ///
    /// Discovery recomputes the legacy sha from `sha256(canonicalize(config_path))`
    /// — the only surviving use of the old hash formula (the new file's V2
    /// discriminant prevents any re-conversion, so leaving a stale legacy file
    /// is harmless, but reaping keeps `$GRIM_HOME` from accreting orphans).
    ///
    /// Idempotent: an already-absent legacy file is a silent no-op. A removal
    /// failure (e.g. a permission error) is swallowed deliberately — the
    /// relocation has already succeeded, so a failed reap must not fail the
    /// command; the worst case is a leftover stale file, never data loss.
    pub fn reap_legacy_project_state(grim_home: &Path, config_path: &Path) {
        let canonical = std::fs::canonicalize(config_path).unwrap_or_else(|_| config_path.to_path_buf());
        let legacy_path = Self::legacy_project_path(&grim_home.join("state"), &canonical);
        if let Err(e) = std::fs::remove_file(&legacy_path)
            && e.kind() != std::io::ErrorKind::NotFound
        {
            tracing::warn!(
                path = %legacy_path.display(),
                error = %e,
                "could not reap legacy project install-state file after relocation"
            );
        }
    }

    /// Persist install state with the project-scope `.grimoire/` lifecycle.
    ///
    /// `ConfigScope::Project`: ensure `.grimoire/` (+ self-managed gitignore),
    /// save, then reap the pre-relocation legacy state file — but ONLY when the
    /// V1→V2 migration was not lossy (a lossy migration keeps the legacy file as
    /// the sole recovery breadcrumb). `ConfigScope::Global`: save only.
    ///
    /// Single seam for every install-state write (3 CLI commands + TUI) so the
    /// lossy-migration reap guard can never diverge across call sites.
    ///
    /// # Errors
    ///
    /// [`PersistError::EnsureDir`] when creating `.grimoire/` (or its
    /// gitignore) fails; [`PersistError::Save`] when the atomic write of the
    /// state file fails. A reap failure is intentionally never surfaced — the
    /// relocation already succeeded, so a stale legacy file is the worst case.
    pub fn persist(
        &self,
        scope: ConfigScope,
        workspace: &Path,
        grim_home: &Path,
        config_path: &Path,
    ) -> Result<(), PersistError> {
        if scope == ConfigScope::Project {
            Self::ensure_project_state_dir(workspace).map_err(|source| PersistError::EnsureDir {
                path: workspace.to_path_buf(),
                source,
            })?;
        }
        // Capture the lossy flag before the write — the reap below is skipped
        // when the V1→V2 conversion dropped any output/record, keeping the
        // legacy file as the sole recovery breadcrumb.
        let lossy = self.legacy_migration_lossy();
        self.save().map_err(|source| PersistError::Save {
            path: self.path.clone(),
            source,
        })?;
        if scope == ConfigScope::Project && !lossy {
            Self::reap_legacy_project_state(grim_home, config_path);
        }
        Ok(())
    }
}

/// Failure persisting install state through [`InstallState::persist`].
#[derive(Debug, thiserror::Error)]
#[non_exhaustive]
pub enum PersistError {
    /// Creating the project `.grimoire/` directory (or its gitignore) failed.
    #[error("failed to create install-state directory {path}")]
    EnsureDir {
        path: PathBuf,
        #[source]
        source: std::io::Error,
    },
    /// The atomic write of the state file failed.
    #[error("failed to save install state {path}")]
    Save {
        path: PathBuf,
        #[source]
        source: std::io::Error,
    },
}

/// Convert V1 wire records into V2 in-memory [`InstallRecord`]s, anchoring
/// every absolute target against `roots` for `scope`. `content_hash` is
/// carried through UNCHANGED so the drift baseline survives the migration.
///
/// Returns the converted records and a lossy flag: `true` when any output
/// or whole record was dropped (unanchorable). A lossy conversion must NOT
/// reap the legacy file — it is the only surviving record of what was lost.
///
/// Per §5 of the migration design:
/// - an empty `clients` list synthesizes one `claude` output from the
///   denormalized top-level `target`/`content_hash`;
/// - each output whose absolute target classifies under no known anchor is
///   dropped after a `tracing::warn!` (never fabricated, never panics);
/// - an output whose `support_dir` is `Some(_)` but fails to anchor is
///   dropped WHOLE (not just its support dir): the stored `content_hash`
///   covered the combined index + support-dir footprint, so keeping the
///   output with a target-only footprint would permanently report the rule
///   as `modified` (false drift). Dropping the whole output is honest — the
///   record is then dropped too if it was the only output;
/// - a record left with zero anchorable outputs is dropped after a warning;
/// - when a **non-empty** V1 file yields **zero** anchorable records (all
///   dropped), a single summary `tracing::warn!` is emitted so the
///   drift-baseline-reset / empty-outcome is visible in logs.
///
/// **Lazy rebuild intentionally omitted.** The design considered re-hashing
/// current on-disk bytes as a fallback when all records are unanchorable.
/// That was rejected as unsafe: it silently resets the drift baseline
/// (user-edited files would appear "clean"), produces no record of the
/// reset, and is indistinguishable from a fresh install in the state file.
/// Callers that need a clean slate should delete `.grimoire/state.json` and
/// re-run `grim install`.
fn convert_v1_records(
    records: Vec<InstallRecordV1>,
    scope: ConfigScope,
    roots: &AnchorRoots,
) -> (Vec<InstallRecord>, bool) {
    let had_records = !records.is_empty();
    let mut lossy = false;
    let mut converted = Vec::with_capacity(records.len());
    for v1 in records {
        // `clients.is_empty() ? [synth claude] : clients` — the denormalized
        // top-level fields are the source only for the earliest V1 files.
        let raw_outputs: Vec<ClientRecordV1> = if v1.clients.is_empty() {
            vec![ClientRecordV1 {
                client: "claude".to_string(),
                target: v1.target.clone(),
                content_hash: v1.content_hash.clone(),
                support_dir: None,
            }]
        } else {
            v1.clients
        };

        let mut outputs = Vec::with_capacity(raw_outputs.len());
        for out in raw_outputs {
            // A client string that does not name a supported target cannot be
            // anchored (the candidate-anchor set is keyed on the target), so
            // drop it the same way as an unanchorable path.
            let Ok(client_target) = ClientTarget::from_str(&out.client) else {
                tracing::warn!(client = %out.client, name = %v1.name, "skipping unknown client in legacy install state");
                lossy = true;
                continue;
            };
            let target = match AnchoredPath::from_target(&out.target, scope, client_target, v1.kind, roots) {
                Ok(anchored) => anchored,
                Err(err) => {
                    tracing::warn!(path = %out.target.display(), error = %err, "skipping unanchorable target in legacy install state");
                    lossy = true;
                    continue;
                }
            };
            // A `support_dir` that fails to anchor cannot be dropped in
            // isolation: the stored `content_hash` covered the combined
            // index + support-dir footprint, so keeping a target-only output
            // would make `current_hash` (target-only) never match — the rule
            // would be reported `modified` forever (false drift). Drop the
            // WHOLE output instead; honest loss beats a permanently-wrong
            // baseline. (A `support_dir: None` output is unaffected.)
            let support_dir = match out.support_dir {
                Some(dir) => match AnchoredPath::from_target(&dir, scope, client_target, v1.kind, roots) {
                    Ok(anchored) => Some(anchored),
                    Err(err) => {
                        tracing::warn!(path = %dir.display(), error = %err, "dropping legacy output whose support dir is unanchorable (combined content_hash would never match a target-only footprint)");
                        lossy = true;
                        continue;
                    }
                },
                None => None,
            };
            outputs.push(ClientOutput {
                client: out.client,
                target,
                content_hash: out.content_hash,
                support_dir,
            });
        }

        if outputs.is_empty() {
            tracing::warn!(name = %v1.name, "dropping legacy install record with no anchorable outputs");
            lossy = true;
            continue;
        }
        converted.push(InstallRecord {
            kind: v1.kind,
            name: v1.name,
            pinned: v1.pinned,
            outputs,
        });
    }

    // Emit a single summary warning when a non-empty V1 file produced no
    // anchorable records at all. Individual per-record warnings have already
    // fired above; this summary makes the empty-outcome visible at a glance
    // (e.g. when tailing logs or filtering for WARN-level messages).
    if had_records && converted.is_empty() {
        tracing::warn!(
            "all records from legacy V1 install state were unanchorable and dropped; \
             the resulting state is empty (drift baseline reset). \
             Run `grim install` to rebuild."
        );
    }

    (converted, lossy)
}

