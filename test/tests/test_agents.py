"""Agent artifact kind acceptance tests.

Agents are single ``.md`` files with required frontmatter (``name``,
``description``; optional ``model``, ``tools``, and a ``metadata`` map with
``<vendor>.<field>`` string keys).  Their OCI ``artifactType`` is
``application/vnd.grimoire.agent.v1``.

Build/release:
- ``grim build --kind agent <file>`` / ``grim release --kind agent <file> <ref>``
  (``--kind`` is REQUIRED for agents; a bare ``.md`` defaults to rule).
- A bare ``.md`` whose frontmatter carries both ``name:`` and ``description:``
  (agent-shaped) emits a stderr warning when built/released without ``--kind``.
- name/stem mismatch → exit 65.
- bad vendor literal (e.g. ``metadata.claude.permission-mode``) → exit 65.

Add / config / lock:
- ``grim add <ref>`` infers kind ``agent`` from the manifest
  ``artifactType``; declares the entry in the ``[agents]`` TOML table;
  the lock gets ``[[agent]]`` entries.

Install targets (project scope, all three clients detected):
- **Claude**: ``.claude/agents/<name>.md`` — verbatim byte-identical when
  no ``claude.*`` metadata keys; generated/cleaned render when
  ``claude.*`` keys are present.
- **OpenCode**: ``.opencode/agents/<name>.md`` — ALWAYS generated:
  frontmatter has ``description`` + ``model`` only (NO ``name:``, NO
  ``tools:``); provenance comment ``<!-- generated by grim from … -->``;
  then body.
- **Copilot**: ``.github/agents/<name>.md`` — ALWAYS generated:
  frontmatter ``name``, ``description``, ``tools`` as YAML list
  (comma-split); NO ``model:``; provenance comment; then body.

Override precedence: common ``model: sonnet`` + metadata
``claude.model: opus`` → Claude file has ``model: opus``; OpenCode file
has ``model: sonnet``.

Global scope: honours ``CLAUDE_CONFIG_DIR``, ``COPILOT_HOME``, and
``XDG_CONFIG_HOME`` env overrides.  Copilot agents land natively with NO
inert-rule warning.

Bundles: a bundle source ``.toml`` may carry ``[agents]``; bundle members
with kind ``"agent"`` round-trip through lock → install and are evicted
when the bundle is removed.
"""
from __future__ import annotations

from pathlib import Path

from src.helpers import make_artifact, make_bundle, write_config
