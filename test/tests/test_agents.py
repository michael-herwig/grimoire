def _setup_agent_config(grim_at, project_dir: Path, unique_repo: str):
    """Publish, declare, lock, and install one agent. Returns (runner, ag)."""
    ag = _push_agent(unique_repo, name="del-agent")
    _detect_all_clients(project_dir)
    write_config(project_dir, agents={"del-agent": ag.fq})
    runner = grim_at(project_dir)
    runner.run("lock")
    runner.run("install")
    return runner, ag


def test_uninstall_agent_undeclares_and_exits_cleanly(
    grim_at, project_dir: Path, registry: str, unique_repo: str
) -> None:
    """``grim uninstall agent <name>`` must exit 0, report the agent
    as uninstalled, and drop the declaration from the config + lock.
    """
    runner, _ = _setup_agent_config(grim_at, project_dir, unique_repo)

    out = runner.json("uninstall", "agent", "del-agent")
    assert out["kind"] == "agent"
    assert out["name"] == "del-agent"
    assert out["status"] == "uninstalled"

    cfg = (project_dir / "grimoire.toml").read_text()
    assert "del-agent" not in cfg, (
        f"uninstall must remove the agent declaration from config:\n{cfg}"
    )
    lock = project_dir / "grimoire.lock"
    if lock.is_file():
        assert "del-agent" not in lock.read_text(), (
            "uninstall must remove the agent from the lock"
        )

    # status no longer knows the artifact.
    status = runner.json("status")
    assert all(r.get("name") != "del-agent" for r in status)


