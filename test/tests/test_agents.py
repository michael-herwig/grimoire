# i. drift: edit generated OpenCode agent → reinstall refused; --force restores
# ---------------------------------------------------------------------------


def test_drift_edited_opencode_agent_refused_then_force_restores(
    grim_at, project_dir: Path, registry: str, unique_repo: str
) -> None:
    """Editing the generated OpenCode agent file after install causes drift.

    A plain ``grim install`` must refuse (exit 65) without overwriting.
    ``--force`` must restore the canonical generated content.
    """
    ag = _push_agent(unique_repo, name="drift-agent")
    _detect_all_clients(project_dir)
    write_config(project_dir, agents={"drift-agent": ag.fq})
    runner = grim_at(project_dir)
    runner.run("lock")
    runner.run("install", "--client", "opencode", format="json")

    oc_file = project_dir / ".opencode/agents/drift-agent.md"
    assert oc_file.is_file(), "agent must be installed before drift test"

    original_provenance_present = "<!-- generated by grim from" in oc_file.read_text()
    assert original_provenance_present, (
        "OpenCode agent file must have provenance comment before edit"
    )

    # Hand-edit the generated file.
    oc_file.write_text("hand edited by user\n")

    # Without --force: must refuse with exit 65.
    refused = runner.run("install", "--client", "opencode", check=False)
    assert refused.returncode == 65, (
        f"drift on generated OpenCode agent must refuse with 65, "
        f"got {refused.returncode}; {refused.stderr}"
    )
    assert oc_file.read_text() == "hand edited by user\n", (
        "a refused install must not overwrite the user's edit"
    )

    # With --force: must restore the canonical generated content.
    forced = runner.run("install", "--client", "opencode", "--force", check=False)
    assert forced.returncode == 0, forced.stderr
    restored = oc_file.read_text()
    assert "<!-- generated by grim from" in restored, (
        f"forced install must restore the generated agent content:\n{restored}"
    )
    assert "hand edited by user" not in restored, (
        "forced install must overwrite the user's edit"
    )


