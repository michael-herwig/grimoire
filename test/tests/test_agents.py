def test_install_agent_per_client_rendering_when_materializer_available(
    grim_at, project_dir: Path, registry: str, unique_repo: str
) -> None:
    """Per-client install rendering contract for agents.

    - Claude: ``.claude/agents/<name>.md`` — verbatim (no claude.* keys).
    - OpenCode: ``.opencode/agents/<name>.md`` — no ``name:``/``tools:``;
      has ``description:`` + ``model:``; has provenance comment.
    - Copilot: ``.github/agents/<name>.md`` — has ``name:``, ``description:``,
      ``tools:`` as YAML list; no ``model:``; has provenance comment.
    """
    import json as _json

    ag = _push_agent(unique_repo, tools="Bash,Read")
    _detect_all_clients(project_dir)
    write_config(project_dir, agents={"my-agent": ag.fq})
    runner = grim_at(project_dir)
    runner.run("lock")

    result = runner.run("install", format="json")
    rows = _json.loads(result.stdout)
    assert any(r["status"] in ("installed", "unchanged") for r in rows), (
        f"install must succeed for at least one client, got: {rows}"
    )

    # Claude: verbatim — all frontmatter fields including name: and tools:.
    claude_file = project_dir / ".claude/agents/my-agent.md"
    assert claude_file.is_file(), (
        "Claude agent file must exist at .claude/agents/my-agent.md"
    )
    claude_text = claude_file.read_text()
    assert "name: my-agent" in claude_text
    assert "description: A test agent." in claude_text
    assert "model: sonnet" in claude_text
    assert "Bash" in claude_text and "Read" in claude_text
    assert "<!-- generated by grim from" not in claude_text, (
        "verbatim Claude agent must not have provenance comment"
    )

    # OpenCode: no name:, no tools:; has description: and model:.
    oc_file = project_dir / ".opencode/agents/my-agent.md"
    assert oc_file.is_file(), (
        "OpenCode agent file must exist at .opencode/agents/my-agent.md"
    )
    oc_text = oc_file.read_text()
    assert "name:" not in oc_text
    assert "tools:" not in oc_text
    assert "description: A test agent." in oc_text
    assert "model: sonnet" in oc_text
    assert "<!-- generated by grim from" in oc_text
    assert "edits will be overwritten" in oc_text

    # Copilot: has name:, description:, tools: as YAML list; no model:.
    cp_file = project_dir / ".github/agents/my-agent.md"
    assert cp_file.is_file(), (
        "Copilot agent file must exist at .github/agents/my-agent.md"
    )
    cp_text = cp_file.read_text()
    assert "name: my-agent" in cp_text
    assert "description: A test agent." in cp_text
    assert "model:" not in cp_text
    assert "Bash" in cp_text
    assert "<!-- generated by grim from" in cp_text


