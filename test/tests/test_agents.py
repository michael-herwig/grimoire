def test_bundle_with_agent_member_locks_and_lock_has_provenance(
    grim_at, project_dir: Path, registry: str, unique_repo: str
) -> None:
    """A bundle whose members include an agent kind entry is locked; the
    lock carries the agent member with bundle provenance and the member
    installs like a direct declaration.
    """
    ag = _push_agent(unique_repo, name="bundle-agent")
    bundle = make_bundle(
        f"{unique_repo}/mixed-stack",
        [("agent", "bundle-agent", ag.fq)],
        tag="1.0.0",
    )
    _detect_all_clients(project_dir)
    write_config(project_dir, bundles={"mixed-stack": bundle.fq})
    runner = grim_at(project_dir)

    runner.run("lock")

    lock = (project_dir / "grimoire.lock").read_text()
    assert "bundle-agent" in lock, (
        f"bundle agent member must appear in the lock:\n{lock}"
    )
    assert "@sha256:" in lock
    assert f"bundle = \"{REGISTRY_HOST}/{unique_repo}/mixed-stack\"" in lock, (
        f"bundle provenance must be recorded in the lock:\n{lock}"
    )

    runner.run("install", format="json")

    # Verify bundle provenance in status.
    status_rows = runner.json("status")
    agent_row = next(
        (r for r in status_rows if r.get("kind") == "agent" and r["name"] == "bundle-agent"),
        None,
    )
    assert agent_row is not None, (
        f"status must include the bundle agent member row, got: {status_rows}"
    )
    assert "bundle:" in agent_row.get("source", ""), (
        f"bundle agent member must have bundle provenance in status, got: {agent_row}"
    )

    claude_file = project_dir / ".claude/agents/bundle-agent.md"
    assert claude_file.is_file(), (
        "bundle agent member must be installed as a Claude agent file"
    )


def test_remove_bundle_evicts_agent_member_from_lock(
    grim_at, project_dir: Path, registry: str, unique_repo: str
) -> None:
    """Removing a bundle that contains an agent member drops the agent
    from the lock (mirrors the existing skill/rule bundle-removal test).
    """
    ag = _push_agent(unique_repo, name="evict-agent")
    bundle = make_bundle(
        f"{unique_repo}/evict-stack",
        [("agent", "evict-agent", ag.fq)],
        tag="1.0.0",
    )
    write_config(project_dir, bundles={"evict-stack": bundle.fq})
    runner = grim_at(project_dir)
    runner.run("lock")
    assert "evict-agent" in (project_dir / "grimoire.lock").read_text()

    out = runner.json("remove", "bundle", "evict-stack")
    assert out["status"] == "removed"

    cfg = (project_dir / "grimoire.toml").read_text()
    assert "evict-stack" not in cfg, "bundle must be undeclared from config"
    lock = (project_dir / "grimoire.lock").read_text()
    assert "evict-agent" not in lock, (
        f"removing the bundle must evict its agent member from the lock:\n{lock}"
    )
