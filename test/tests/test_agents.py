# ---------------------------------------------------------------------------
# Local helpers
# ---------------------------------------------------------------------------


def _write(p: Path, body: str) -> None:
    p.parent.mkdir(parents=True, exist_ok=True)
    p.write_text(body)


def _agent_doc(
    name: str = "my-agent",
    model: str = "sonnet",
    tools: str = "Bash,Read",
    extra_metadata: str = "",
) -> str:
    """A canonical agent frontmatter document.

    ``tools`` is a top-level common field (like ``model``); vendor keys go
    into the ``metadata`` map via ``extra_metadata``.
    """
    tools_line = f"tools: {tools}\n" if tools else ""
    meta_block = f"metadata:\n{extra_metadata}" if extra_metadata else ""
    return (
        f"---\n"
        f"name: {name}\n"
        f"description: A test agent.\n"
        f"model: {model}\n"
        f"{tools_line}"
        f"{meta_block}"
        f"---\n"
        f"# {name}\n"
        f"Agent body text.\n"
    )


def _push_agent(
    unique_repo: str,
    name: str = "my-agent",
    model: str = "sonnet",
    tools: str = "Bash,Read",
    extra_metadata: str = "",
    tag: str = "v1",
) -> "src.registry.PublishedArtifact":
    """Push a minimal agent artifact and return the published reference."""
    return make_artifact(
        f"{unique_repo}/{name}",
        "agent",
        {f"{name}.md": _agent_doc(name, model, tools, extra_metadata)},
        tag=tag,
    )


def _detect_all_clients(project_dir: Path) -> None:
    """Create vendor markers so all three clients are detected."""
    (project_dir / ".claude").mkdir(parents=True, exist_ok=True)
    (project_dir / ".opencode").mkdir(parents=True, exist_ok=True)
    (project_dir / ".github" / "instructions").mkdir(parents=True, exist_ok=True)


# ---------------------------------------------------------------------------
# a. release --kind agent publishes with agent artifactType
# ---------------------------------------------------------------------------


def test_release_with_kind_agent_publishes_agent_artifact_type(
    grim_at, project_dir: Path, registry: str, unique_repo: str
) -> None:
    """``grim release --kind agent`` pushes with
    ``artifactType = application/vnd.grimoire.agent.v1``.

    Uses ``fetch_manifest`` (the same wire-contract verification used in
    ``test_add_infer.py``) and ``grim add`` kind inference to assert the
    OCI manifest carries the correct type.
    """
    agent_file = project_dir / "my-agent.md"
    _write(agent_file, _agent_doc("my-agent"))
    repo_path = f"{unique_repo}/my-agent"
    repo = f"{registry}/{repo_path}"
    runner = grim_at(project_dir)

    out = runner.json("release", "--kind", "agent", str(agent_file), f"{repo}:1.0.0")
    assert out["pushed"] is True

    # Wire-level assertion: manifest carries the agent artifactType.
    manifest = fetch_manifest(repo_path, "1.0.0")
    assert manifest["artifactType"] == "application/vnd.grimoire.agent.v1", (
        f"agent release must set artifactType=application/vnd.grimoire.agent.v1, "
        f"got {manifest.get('artifactType')!r}"
    )
    assert manifest["config"]["mediaType"] == "application/vnd.grimoire.agent.config.v1+json", (
        f"config mediaType must be the agent config type, "
        f"got {manifest['config']['mediaType']!r}"
    )

    # End-to-end: grim add infers kind "agent" from the manifest.
    write_config(project_dir)
    out_add = runner.json("add", f"{repo}:1.0.0")
    assert out_add["kind"] == "agent", (
        f"grim add must infer kind 'agent' from the manifest, got {out_add['kind']!r}"
    )


# ---------------------------------------------------------------------------
# b. bare agent-shaped .md WITHOUT --kind → rule + stderr warning
# ---------------------------------------------------------------------------


def test_bare_agent_shaped_md_without_kind_builds_as_rule_with_warning(
    grim_at, project_dir: Path
) -> None:
    """A ``.md`` with both ``name:`` and ``description:`` (agent-shaped)
    released WITHOUT ``--kind`` is treated as a rule; stderr carries a
    warning that the file "looks like an agent definition".
    """
    agent_file = project_dir / "my-agent.md"
    _write(agent_file, _agent_doc("my-agent"))
    runner = grim_at(project_dir)

    # Use JSON format so we can assert the output kind if the build succeeds.
    result = runner.run("build", str(agent_file), format="json", check=False)
    # The key assertion is the stderr warning.
    assert "looks like an agent" in result.stderr.lower(), (
        f"building an agent-shaped .md without --kind must warn on stderr; "
        f"got stderr: {result.stderr!r}"
    )
    # If it succeeded, the kind in stdout must be "rule", not "agent".
    if result.returncode == 0:
        import json as _json
        out = _json.loads(result.stdout)
        assert out["kind"] == "rule", (
            f"without --kind, an agent-shaped .md must be built as 'rule', "
            f"got {out['kind']!r}"
        )


